class RecomendadorLLM:
    def __init__(self):
        self.estado = "inicio"
        self.tipo_seleccionado = None

    def _call(self, prompt: str) -> str:
        texto = prompt.lower().strip()

        # INICIO
        if self.estado == "inicio":
            self.estado = "tipo_bebida"
            return "üëã ¬°Hola! Bienvenido/a soy tu Asistente virtual. ¬øQuer√©s caf√© o t√© hoy?"

        # ELEGIR CAFE O TE
        elif self.estado == "tipo_bebida":
            if "caf√©" in texto or "cafe" in texto:
                self.tipo_seleccionado = "caf√©"
                self.estado = "mostrar_menu"
                return "Perfecto ‚òï Te muestro nuestra carta de caf√©s:"

            elif "t√©" in texto or "te" in texto:
                self.tipo_seleccionado = "t√©"
                self.estado = "mostrar_menu"
                return "Genial üçµ Te muestro nuestra carta de t√©s:"

            return "Decime si prefer√≠s *caf√©* o *t√©*."

        # ESPERA QUE EL USUARIO ELIJA DESDE LA CARTA
        elif self.estado == "mostrar_menu":
            return "Eleg√≠ tu bebida tocando un bot√≥n en la carta. üòÑ"

        # CONFIRMAR COMPRA
        elif self.estado == "confirmar_compra":
            if "si" in texto or "s√≠" in texto or "comprar" in texto:
                self.estado = "inicio"
                return (
                    "üéâ ¬°Gracias por tu compra! En minutos estar√° listo tu pedido.\n"
                    "üîÑ Reiniciando asistente..."
                )
            elif "no" in texto:
                self.estado = "inicio"
                return "Todo bien üòä Si quer√©s otra recomendaci√≥n, escribime."
            return "Decime *s√≠* para comprar o *no* para cancelar."

        return "No entend√≠, ¬øpod√©s repetirlo?"


